<template>
  <view class="uni-container">
    <uni-forms ref="form" :value="formData" validate-trigger="submit" err-show-type="toast">
      <uni-forms-item name="game_name" label="游戏名称" required>
        <uni-easyinput placeholder="游戏名称" v-model="formData.game_name"></uni-easyinput>
      </uni-forms-item>
      <uni-forms-item name="picture" label="游戏封面">
        <uni-file-picker file-mediatype="image" file-extname="jpg,png" return-type="object" v-model="formData.picture"></uni-file-picker>
      </uni-forms-item>
      <uni-forms-item name="regions" label="区服列表">
        <uni-data-checkbox :multiple="true" v-model="formData.regions" collection="speednet-game-region" field="region_name as text, _id as value"></uni-data-checkbox>
      </uni-forms-item>
      <uni-forms-item name="platform" label="游戏平台">
        <uni-data-checkbox v-model="formData.platform" :localdata="formOptions.platform_localdata"></uni-data-checkbox>
      </uni-forms-item>
      <view class="uni-button-group">
        <button type="primary" class="uni-button" @click="submit">提交</button>
      </view>
    </uni-forms>
  </view>
</template>

<script>
  import { validator } from '../../js_sdk/validator/speednet-game.js';

  const db = uniCloud.database();
  const dbCollectionName = 'speednet-game';

  function getValidator(fields) {
    let result = {}
    for (let key in validator) {
      if (fields.indexOf(key) > -1) {
        result[key] = validator[key]
      }
    }
    return result
  }

  export default {
    data() {
      let formData = {
        "game_name": "",
        "picture": null,
        "regions": [],
        "platform": null
      }
      return {
        formData,
        formOptions: {
          "platform_localdata": [
            {
              "value": 1,
              "text": "PC"
            },
            {
              "value": 2,
              "text": "Android"
            }
          ]
        },
        rules: {
          ...getValidator(Object.keys(formData))
        }
      }
    },
    onLoad(e) {
      if (e.id) {
        const id = e.id
        this.formDataId = id
        this.getDetail(id)
      }
    },
    onReady() {
      this.$refs.form.setRules(this.rules)
    },
    methods: {
      /**
       * 验证表单并提交
       */
      submit() {
        uni.showLoading({
          mask: true
        })
        this.$refs.form.validate().then((res) => {
          return this.submitForm(res)
        }).catch(() => {
        }).finally(() => {
          uni.hideLoading()
        })
      },

      /**
       * 提交表单
       */
      submitForm(value) {
        // 使用 clientDB 提交数据
        return db.collection(dbCollectionName).doc(this.formDataId).update(value).then((res) => {
          uni.showToast({
            icon: 'none',
            title: '修改成功'
          })
          this.getOpenerEventChannel().emit('refreshData')
          setTimeout(() => uni.navigateBack(), 500)
        }).catch((err) => {
          uni.showModal({
            content: err.message || '请求服务失败',
            showCancel: false
          })
        })
      },

      /**
       * 获取表单数据
       * @param {Object} id
       */
      getDetail(id) {
        uni.showLoading({
          mask: true
        })
        db.collection(dbCollectionName).doc(id).field("game_name,picture,regions,platform").get().then((res) => {
          const data = res.result.data[0]
          if (data) {
            this.formData = data
          }
        }).catch((err) => {
          uni.showModal({
            content: err.message || '请求服务失败',
            showCancel: false
          })
        }).finally(() => {
          uni.hideLoading()
        })
      }
    }
  }
</script>

